     private void clearNotice(){
      
      jTextField6.setText(null);
      jTextField7.setText(null);
      jTextField9.setText(null);
      jTable2.clearSelection();
    }
     
     public boolean isEmptyNotice(){
        
      if(jTextField6.getText().isEmpty()){
        JOptionPane.showMessageDialog( this, "Notice Number is missing!");
        return false;
      }
      
      if(jTextField7.getText().isEmpty()){
        JOptionPane.showMessageDialog( this, "Notice Date is missing!");
        return false;
      }
      
      if(jTextField9.getText().isEmpty()){
        JOptionPane.showMessageDialog( this, "Notice Title is missing!");
        return false;
      }
      return true;
      
    }

