    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if(isEmptyNotice()){
            int nnum = Integer.parseInt(jTextField6.getText());
            String ndate = jTextField7.getText();
            String ntitle = jTextField9.getText();

            notice.insert(nnum, ndate, ntitle);

            jTable2.setModel(new DefaultTableModel(null, new Object[]{"Number","Date","Title"}));
            notice.getTableValue(jTable2, "");
            clearNotice();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

